package Fundamentos;
//Operadores de atribuição

public class Fundamento21 {
	public static void main(String[] args){
		
		int a = 3;
		int b = a;
		int c = a + b;
		
		c++; // c = c + 1; Operador unário
		
		c += b; // c = c + b;
		c -= a; // c = c - a;
		c *= b; // c = c * b;
		c /= a; // c = c / a;
		c %= a; // c = c % a;

	}

}
