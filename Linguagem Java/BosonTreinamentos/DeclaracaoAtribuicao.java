package BosonTreinamentos;

public class DeclaracaoAtribuicao {
	
	public static void main(String[] args) {
		
		boolean VouF = true;
		byte a;
		char letra = 'F';
		int valor = 4500;
		double pi = 3.14;
		a = 20;
		
		System.out.printf("Valor de V ou F (BOOLEAN): %b\n",VouF);
		System.out.printf("Valor de a (BYTE): %dn",a);
		System.out.printf("Valor de letra (CHAR): %c\n",letra);
		System.out.printf("Valor de valor (INT): %d\n",valor);
		System.out.printf("Valor de pi (DOUBLE): %2.2f",pi);
			
	}

}
